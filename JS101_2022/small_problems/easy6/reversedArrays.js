function reverse(array) {
  let arrCopy = array.slice();
  let arrayValue = array.length - 1;
  for (let index = 0; index < array.length; index++) {
    array[index] = arrCopy[arrayValue];
    arrayValue -= 1;
  }
  return array;
}


